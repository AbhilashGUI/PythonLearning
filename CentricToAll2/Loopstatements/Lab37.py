#While loop is used to execute a statement , till the condition is true. It test the condition before executing the loop body.
#It is preferred, when the iterations are unknown


Count=1
while Count<=5:
    print(Count)           #It is an incrementing order
    Count += 1
print("Out of the loop")


Count=5
while Count>=1:
    print(Count)       #It is a decrementing order
    Count-=1
print("Out of the loop")