#Logical operators#

a=10
b=20
c=30
d=False
print(a>5 and b<=20)              #Note: For AND operators both the conditions, should be true
print(b>a and c<10)               #Since,One condition is false the output is shown as false




print(a>10 or b<a)                #Note:For OR operators both the conditions are false the output is shown as false
print(a<b or c>a)                 #Note: Any one condition, should be true.


print(not(d))                     #Note:Not Operator,returns the opposite of the defined one


